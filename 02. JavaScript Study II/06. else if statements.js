var level = 9;

// Print "Can handle ninja swords!" when level is greater than or equal to 5, and less than 10
if (level >= 10) {
  console.log("Can handle throwing stars and ninja swords!");
} else if (level >= 5 && level < 10) {
  console.log("Can handle ninja swords!");
} else {
  console.log("Can't handle any weapons");
}
